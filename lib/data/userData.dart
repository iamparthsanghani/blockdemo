        this.itemTotalAmount,
        this.deliveryFees,
        this.createdAt,
        this.itemCount,
        this.businessName,
        this.businessAddress,
        this.profilePic});

  User.fromJson(Map<String, dynamic> json) {
    orderId = json['order_id'];
    driverId = json['driver_id'];
    orderStatus = json['order_status'];
    userId = json['user_id'];
    addressId = json['address_id'];
    notes = json['notes'];
    businessId = json['business_id'];
    acceptedByBusiness = json['accepted_by_business'];
    pickupTime = json['pickup_time'];
    deliveryTime = json['delivery_time'];
    totalPayableAmount = json['total_payable_amount'];
    sizeId = json['size_id'];
    totalDiscount = json['total_discount'];
    orderTime = json['order_time'];
    itemTotalAmount = json['item_total_amount'];
    deliveryFees = json['delivery_fees'];
    createdAt = json['created_at'];
    itemCount = json['item_count'];
    businessName = json['business_name'];
    businessAddress = json['business_address'];
    profilePic = json['profile_pic'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['order_id'] = this.orderId;
    data['driver_id'] = this.driverId;
    data['order_status'] = this.orderStatus;
    data['user_id'] = this.userId;
    data['address_id'] = this.addressId;
    data['notes'] = this.notes;
    data['business_id'] = this.businessId;
    data['accepted_by_business'] = this.acceptedByBusiness;
    data['pickup_time'] = this.pickupTime;
    data['delivery_time'] = this.deliveryTime;
    data['total_payable_amount'] = this.totalPayableAmount;
    data['size_id'] = this.sizeId;
    data['total_discount'] = this.totalDiscount;
    data['order_time'] = this.orderTime;
    data['item_total_amount'] = this.itemTotalAmount;
    data['delivery_fees'] = this.deliveryFees;
    data['created_at'] = this.createdAt;
    data['item_count'] = this.itemCount;
    data['business_name'] = this.businessName;
    data['business_address'] = this.businessAddress;
    data['profile_pic'] = this.profilePic;
    return data;
  }
}
