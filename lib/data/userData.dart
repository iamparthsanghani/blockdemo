
import 'dart:convert';

String albumToJson(List<User> data) =>
    json.encode(List<dynamic>.from(data.map((x) => x.toJson())));

class Autogenerated {
  List<User> info;
  String message;
  int status;

  Autogenerated({this.info, this.message, this.status});

  Autogenerated.fromJson(Map<String, dynamic> json) {
    if (json['info'] != null) {
      info = new List<User>();
      json['info'].forEach((v) {
        info.add(new User.fromJson(v));
      });
    }
    message = json['message'];
    status = json['status'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    if (this.info != null) {
      data['info'] = this.info.map((v) => v.toJson()).toList();
    }
    data['message'] = this.message;
    data['status'] = this.status;
    return data;
  }
}

class User {
  int orderId;
  int driverId;
  int orderStatus;
  int userId;
  int addressId;
  Null notes;
  int businessId;
  String acceptedByBusiness;
  String pickupTime;
  String deliveryTime;
  String totalPayableAmount;
  Null sizeId;
  int totalDiscount;
  String orderTime;
  String itemTotalAmount;
  String deliveryFees;
